def group_names_by_country(data: str = data) -> defaultdict:
    countries = defaultdict(list)
    lines = [line for line in data.split('\n')]
    lines = lines[1:]
    for entry in lines:
        last, first, key = entry.split(',')
        name = first + ' ' + last
        countries[key].append(name)
    return countries